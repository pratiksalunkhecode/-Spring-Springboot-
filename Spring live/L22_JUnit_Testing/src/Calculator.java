
public class Calculator {
	
	public int add(int a, int b) {
		return a+b;
		
	}
	
	public int diff(int a, int b) {
		return a-b;
		
	}
	
	public int calculateInterest(int p,int r, int t) {
		return p*r*t/100;
	}
	
	public String greet(String name) {
		return "Hello "+name+",";
	}

}
